<p>Jeff Marsten - <i>7th Dan Kyoshi</i><br />
Founding member of the UW club back in 1972. Currently the head instructor for Bellevue, Highline
and Sno-King Kendo clubs, he also assists at the UW on a weekly basis, is an advisor to the PNKF
Board of Directors and Coach for the 2002 and 2005 PNKF teams to the National Championships. Married
to Glennis, who doesn’t practice Kendo, but has two out of four kids that do, (Elizabeth-4th Dan
and Jeffy-3rd Dan). A mechanical engineer at Boeing, he’s feverently looking forward to retirement,
so he can teach MORE Kendo.</p>

